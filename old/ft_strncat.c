char	*ft_strncat(char *dest, char *src, int nb)
{
	int		i;
	int		d;

	i = 0;
	d = 0;
	while (dest[d])
		d++;
	while (src[i] && i < nb)
	{
		dest[i + d] = src[i];
		i++;
	}
	dest[i + d] = 0;
	return (dest);
}
