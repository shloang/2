#include <string.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int			i;
	const char	*t1;
	const char	*t2;

	i = 0;
	t1 = s1;
	t2 = s2;
	while (n > 0 && *t1 != 0 && *t2 != 0 && *t1 == *t2)
	{
		t1++;
		t2++;
	}
	i = *t1 - *t2;
	return (i);
}
