char	*ft_strcat(char *dest, const char *src)
{
	int		i;
	int		d;

	i = 0;
	d = 0;
	while (dest[d])
		d++;
	while (src[i])
	{
		dest[i + d] = src[i];
		i++;
	}
	dest[i + d] = src[i];
	return (dest);
}
