void	*ft_memalloc(size_t size)
{
	char	*c;
	size_t	i;

	i = 0;
	if (c = malloc(size))
	{
		while (i < size)
		{
			*(c + i) = 0;
			i++;
		}
		return ((void *)c);
	}
	return (0);
}
