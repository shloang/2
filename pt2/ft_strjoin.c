#include "../libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*s;
	int		l1;
	int		l2;

	l1 = 0;
	l2 = 0;
	if (s1 && s2)
	{
		while (*(s1 + l1) != 0)
			l1++;
		while (*(s2 + l2) != 0)
			l2++;
		if ((s = (char *)malloc(l1 + l2 + 1)))
		{
			*(s + l1 + l2) = 0;
			while (--l2 >= 0)
				*(s + l1 + l2) = *(s2 + l2);
			while (--l1 >= 0)
				*(s + l1) = *(s1 + l1);
			return (s);
		}
	}
	return (0);
}
